-- サブクエリ
SELECT	shohin_bunrui, cnt_shohin
FROM	(
	SELECT	shohin_bunrui, COUNT(*) AS cnt_shohin
	FROM	Shohin
	GROUP BY shohin_bunrui
		) AS ShohinSum;

-- サブクエリの階層数を増やす
SELECT	shohin_bunrui, cnt_shohin
FROM	(
	SELECT	*
		FROM	(
			SELECT		shohin_bunrui, COUNT(*) AS cnt_shohin
			FROM		Shohin
			GROUP BY	shohin_bunrui) AS ShohinSum
		WHERE cnt_shohin = 4) AS ShohinSum2;